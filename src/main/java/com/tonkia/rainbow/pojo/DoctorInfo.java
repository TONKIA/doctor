package com.tonkia.rainbow.pojo;

public class DoctorInfo {
    private Integer uid;
    private String nikeName;
    private String avator;
    private Integer star;
    private String qualificate;
    private Float favorRate;
    private Integer cmtCount;
    private String expert;
    private String label;
    private String[] labelArray;
    private String hospital;

    public String getHospital() {
        return hospital;
    }

    public void setHospital(String hospital) {
        this.hospital = hospital;
    }

    public Integer getUid() {
        return uid;
    }

    public void setUid(Integer uid) {
        this.uid = uid;
    }

    public String getNikeName() {
        return nikeName;
    }

    public void setNikeName(String nikeName) {
        this.nikeName = nikeName;
    }

    public String getAvator() {
        return avator;
    }

    public void setAvator(String avator) {
        this.avator = avator;
    }

    public Integer getStar() {
        return star;
    }

    public void setStar(Integer star) {
        this.star = star;
    }

    public String getQualificate() {
        return qualificate;
    }

    public void setQualificate(String qualificate) {
        this.qualificate = qualificate;
    }

    public Float getFavorRate() {
        return favorRate;
    }

    public void setFavorRate(Float favorRate) {
        this.favorRate = favorRate;
    }

    public Integer getCmtCount() {
        return cmtCount;
    }

    public void setCmtCount(Integer cmtCount) {
        this.cmtCount = cmtCount;
    }

    public String getExpert() {
        return expert;
    }

    public void setExpert(String expert) {
        this.expert = expert;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public String[] getLabelArray() {
        return labelArray;
    }

    public void setLabelArray(String[] labelArray) {
        this.labelArray = labelArray;
    }
}
